adicao = 10 + 10
print('Adição: ', adicao)

subtracao = 10 - 5
print('Subtração: ', subtracao)

multiplicacao = 10 * 10
print('Multiplicação: ', multiplicacao)

divisao = 10 / 3  # O retorno sempre é float
print('Divisão: ', divisao)

divisao_inteira = 10 // 3 # Nem sempre // retona int, se a conta utilizou algum float, o resultado será float
print('Divisão inteira: ', divisao_inteira) # NÃO RETORNA AS CASAS DECIMAIS

exponenciacao = 2 ** 10
print('Exponenciação: ', exponenciacao)

modulo = 55 % 2  # resto da divisão
print('Módulo: ', modulo)

print(10 % 8 == 0) # sabe se é multiplo de 8
print(16 % 8 == 0)
print(10 % 2 == 0) # sabe se o numero é par ou não, nesse caso é PAR
